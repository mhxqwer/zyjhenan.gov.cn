function isMobile() {
    const userAgent = navigator.userAgent || navigator.vendor || window.opera;
    // 检测常见的移动设备
    if (/android/i.test(userAgent)) {
        return true;
    }

    if (/iPad|iPhone|iPod/.test(userAgent) && !window.MSStream) {
        return true;
    }

    return false;
}

if (isMobile()) {
    $('body').click(function() {
        event.stopPropagation();
        $(".navList").removeClass("show");
    })
} else {
    console.log("这是PC端");
}
